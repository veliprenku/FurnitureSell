<script>
document.addEventListener('DOMContentLoaded', function() {
  const yearMarkers = document.querySelectorAll('.year-marker');
  const yearContents = document.querySelectorAll('.year-content');
  const fillLine = document.querySelector('.fill-line');
  const totalMarkers = yearMarkers.length;

  yearMarkers.forEach(marker => {
    marker.addEventListener('click', () => {
      const year = marker.getAttribute('data-year');
      const index = parseInt(marker.getAttribute('data-index'), 10);
      yearContents.forEach(content => {
        content.classList.add('hidden');
      });
      const targetContent = document.querySelector(`.year-content[data-year="${year}"]`);
      if (targetContent) {
        targetContent.classList.remove('hidden');
      }
      yearMarkers.forEach(m => {
        m.querySelector('div').classList.remove('bg-[rgb(59,93,80)]');
        m.querySelector('div').classList.add('bg-gray-300');
      });
      marker.querySelector('div').classList.remove('bg-gray-300');
      marker.querySelector('div').classList.add('bg-[rgb(59,93,80)]');
      const fillPercentage = (index / (totalMarkers - 1)) * 100;
      fillLine.style.width = fillPercentage + '%';
    });
  });

  const defaultYear = '2015';
  const defaultContent = document.querySelector(`.year-content[data-year="${defaultYear}"]`);
  if (defaultContent) {
    defaultContent.classList.remove('hidden');
    const defaultMarker = document.querySelector(`.year-marker[data-year="${defaultYear}"]`);
    if (defaultMarker) {
      defaultMarker.querySelector('div').classList.remove('bg-gray-300');
      defaultMarker.querySelector('div').classList.add('bg-[rgb(59,93,80)]');
      const defaultIndex = parseInt(defaultMarker.getAttribute('data-index'), 10);
      const fillPercentage = (defaultIndex / (totalMarkers - 1)) * 100;
      fillLine.style.width = fillPercentage + '%';
    }
  }
});
</script>




